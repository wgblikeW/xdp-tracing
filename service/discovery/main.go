package main

import (
	"context"
	"fmt"
	"time"

	clientv3 "go.etcd.io/etcd/client/v3"
)

func main() {
	cli, _ := clientv3.New(clientv3.Config{
		Endpoints:   []string{"localhost:2379", "localhost:22379", "localhost:32379"},
		DialTimeout: 5 * time.Second,
	})
	ctx := context.TODO()
	resp, _ := cli.Get(ctx, "node", clientv3.WithPrefix())
	fmt.Print(resp.Kvs)
}
